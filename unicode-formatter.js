/**
 * Converts text to specified Unicode style
 * @param {string} text - The text to convert
 * @param {string} style - The style to apply ('normal', 'bold', 'italic', 'script')
 * @returns {string} - The formatted text
 */
function formatText(text, style) {
    if (!text || typeof text !== 'string') {
        return text;
    }
    
    if (style === 'normal') {
        return text; // Return as-is for normal style
    }
    
    let styleMap;
    switch (style) {
        case 'bold':
            styleMap = BOLD_MAP;
            break;
        case 'italic':
            styleMap = ITALIC_MAP;
            break;
        case 'script':
            styleMap = SCRIPT_MAP;
            break;
        default:
            return text; // Unknown style, return as-is
    }
    
    return text.split('').map(char => {
        return styleMap[char] || char;
    }).join('');
}

